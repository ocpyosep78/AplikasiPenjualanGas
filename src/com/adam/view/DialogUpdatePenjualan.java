    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        pjl.setIdTrskPjl(Integer.parseInt(txtIdTrsk.getText()));
        pjl.setTglTrskPjl(txtDateTrsk.getSelectedDate().getTime());
        pjl.setQtyPjl(Integer.parseInt(cboBanyak.getSelectedItem().toString()));
        pjl.setHrgSatuan(Float.valueOf(TxtSatuanTrsk.getText()));
        pjl.setSubTotal(Float.valueOf(lblJml.getText()));
        
        pDao.update(pjl);
        intPjl.refresh();
        dispose();
    }//GEN-LAST:event_btnUpdateActionPerformed

