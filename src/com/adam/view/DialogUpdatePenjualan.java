    private void cboBanyakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboBanyakActionPerformed
        if(TxtSatuanTrsk.getText().equals("")){
            
        }else{
            int bnyk = Integer.parseInt(cboBanyak.getSelectedItem().toString());
            float satuan = Float.valueOf(TxtSatuanTrsk.getText());
            float jml = getJumlah(bnyk, satuan);
            lblJml.setText(""+jml);
        }
    }//GEN-LAST:event_cboBanyakActionPerformed
    
    public float getJumlah(int qty, float harga){
        float jml = qty*harga;
        return jml;
    }
